let my_obj = {
  name: "Jason",
  location: "Texas",
  age: 24,
  favorite_food: "Pizza"
};

function pretty_print() {
  console.log("Name:          " + my_obj.name);
  console.log("Location:      " + my_obj.location);
  console.log("Age:           " + my_obj.age);
  console.log("Favorite Food: " + my_obj.favorite_food);
}

pretty_print(my_obj)
